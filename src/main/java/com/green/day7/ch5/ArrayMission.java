package com.green.day7.ch5;

public class ArrayMission {
    public static void main(String[] args) {
        int[] arr = {100,200,300,400,500};
        //for문 arr을 이용 배열의 모든방의 값을 출력하시오
        //100 200 300 400

        for(int i=0; i<arr.length ;i++){
            System.out.println(arr[i]);
        }
    }
}
