package com.green.day3.ch4;

public class IfElseIf {
    public static void main(String[] args) {
        //else if 는 그룹if

        if(1 == 4) {
            System.out.println("1");
        } else if(2 == 4){
            System.out.println("2");
        } else if(3 == 4) {
            System.out.println("3");
        } else {
            System.out.println("else");
        }
    }
}
