package com.green.Day4.ch4;

public class Switch {//else와비슷, 같은값 있으면 안됨 ,
    public static void main(String[] args) {
        int val = 1;

        switch(val){
            case 1:
                System.out.println("1이다");
                break;
            case 2:
                System.out.printf("2이다");
                break;
            case 3:
                System.out.println("3이다");
                break;
            default:
                System.out.printf("1~3이 아니다.");





        }

             System.out.println("--끝--");
    }



}
