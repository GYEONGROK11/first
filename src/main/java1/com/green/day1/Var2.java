package com.green.day1;

public class Var2 {
    public static void main(String[]args){
        int x = 10, y=20;
        int temp = x;
        x = y;
        y = temp;
        System.out.println("x: " +x);
        System.out.println("y: "+y);
//*스와핑 -> x y TEMP 라는 컵을 돌려막기하다.
    }
}
